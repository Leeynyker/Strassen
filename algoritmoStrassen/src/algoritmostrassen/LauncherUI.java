//            for(int h = 0;h<cA;h++){
//                System.out.print(" "+matrizA[g][h]);
//               
//            }
//        }
        double matrizB[][] = new double[fB][cB];
        double listamatB[] = new double[fB * cB];

        int j = 0;
        for (JTextField txtb : MlistB) {
            listamatB[j] = Double.parseDouble(txtb.getText());
            j++;
        }

        int countB = 0;
        for (int i2 = 0; i2 < fB; i2++) {
            for (int j2 = 0; j2 < cB; j2++) {
                matrizB[i2][j2] = listamatB[countB];
                countB++;
            }
        }

