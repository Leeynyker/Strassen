    private void btnEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarActionPerformed
        int fA = Integer.parseInt(txtFa.getText());
        int cA = Integer.parseInt(txtCa.getText());
        int fB = Integer.parseInt(txtFb.getText());
        int cB = Integer.parseInt(txtCb.getText());
        if (cA == fB) {
            for (int i = 0; i < fA; i++) {
                for (int j = 0; j < cA; j++) {
                    JTextField txta = new JTextField();
                    txta.setBounds(35 * j, 35 * i, 30, 30);
                    jPaneA.add(txta);
                    MlistA.add(txta);
                }
            }

            for (int i = 0; i < fB; i++) {
                for (int j = 0; j < cB; j++) {
                    JTextField txtb = new JTextField();
                    txtb.setBounds(35 * j, 35 * i, 30, 30);
                    jPaneB.add(txtb);
                    MlistB.add(txtb);
                }
            }

            jPaneA.updateUI();
            jPaneB.updateUI();
        } else {
            JOptionPane.showMessageDialog(null, "Las matrices no se pueden operar");
            txtCa.setText("");
            txtFa.setText("");
            txtCb.setText("");
            txtFb.setText("");
        }
    }//GEN-LAST:event_btnEnviarActionPerformed

